package cn.panshihao.pos.model;

public class Firm {
	
	private int firm_id;
	private String firm_name;
	private String firm_desc;
	private String firm_address;
	private String firm_phone;
	private String firm_person;
	public int getFirm_id() {
		return firm_id;
	}
	public void setFirm_id(int firm_id) {
		this.firm_id = firm_id;
	}
	public String getFirm_name() {
		return firm_name;
	}
	public void setFirm_name(String firm_name) {
		this.firm_name = firm_name;
	}
	public String getFirm_desc() {
		return firm_desc;
	}
	public void setFirm_desc(String firm_desc) {
		this.firm_desc = firm_desc;
	}
	public String getFirm_address() {
		return firm_address;
	}
	public void setFirm_address(String firm_address) {
		this.firm_address = firm_address;
	}
	public String getFirm_phone() {
		return firm_phone;
	}
	public void setFirm_phone(String firm_phone) {
		this.firm_phone = firm_phone;
	}
	public String getFirm_person() {
		return firm_person;
	}
	public void setFirm_person(String firm_person) {
		this.firm_person = firm_person;
	}
	
	
	
}
